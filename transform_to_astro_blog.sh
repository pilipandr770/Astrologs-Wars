#!/bin/bash
# Скрипт для трансформации сайта в астрологический блог

echo "===== ТРАНСФОРМАЦИЯ САЙТА В АСТРОЛОГИЧЕСКИЙ БЛОГ ====="
echo
echo "Этот скрипт выполнит следующие действия:"
echo "1. Удаление функциональности блокчейна/токенов"
echo "2. Улучшение автоматизации блога"
echo "3. Настройка блогов под разные астрологические системы"
echo "4. Обновление внешнего вида главной страницы"
echo
echo "ВНИМАНИЕ: Перед запуском убедитесь, что создали резервную копию базы данных!"
echo
read -p "Нажмите Enter для продолжения..."

# Создаем резервную копию базы данных
echo "Создаем резервную копию базы данных..."
mkdir -p instance/backups
cp instance/app.db instance/backups/app.db.$(date +%Y%m%d).bak

# Удаление функциональности токенов
echo
echo "Шаг 1: Удаление функциональности блокчейна/токенов..."
python remove_token_functionality.py

# Улучшение автоматизации блога
echo
echo "Шаг 2: Улучшение автоматизации блога..."
python enhance_blog_automation.py

# Настройка блогов под разные астрологические системы
echo
echo "Шаг 3: Настройка блогов под разные астрологические системы..."
python setup_astro_blogs.py

echo
echo "Трансформация успешно завершена!"
echo "Рекомендуется перезапустить приложение для применения всех изменений."
echo
